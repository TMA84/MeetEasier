const normalizeAdminTranslations = (rawTranslations) => {
  const normalized = {};
  if (!rawTranslations || typeof rawTranslations !== 'object' || Array.isArray(rawTranslations)) {
    return normalized;
  }

  Object.entries(rawTranslations).forEach(([langKey, value]) => {
    if (!value || typeof value !== 'object' || Array.isArray(value)) {
      return;
    }

    const language = String(langKey).trim().toLowerCase();
    if (!language) {
      return;
    }

    const languageMessages = {};
    Object.entries(value).forEach(([key, textValue]) => {
      languageMessages[String(key)] = textValue !== undefined && textValue !== null ? String(textValue) : '';
    });

    normalized[language] = languageMessages;
  });

  return normalized;
};

export const getAdminLanguage = () => {
  const browserLang = navigator.language || navigator.userLanguage || 'en';
  return browserLang.split('-')[0].toLowerCase();
};

export const getDefaultAdminTranslations = () => defaultAdminTranslations;

export const getAdminTranslations = (rawOverrides) => {
  const overrides = normalizeAdminTranslations(rawOverrides);
  const merged = {
    ...defaultAdminTranslations,
    ...overrides
  };

  const language = getAdminLanguage();
  return {
    ...merged.en,
    ...(merged[language] || {})
  };
};

export default defaultAdminTranslations;